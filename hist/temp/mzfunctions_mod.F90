 subroutine zmh_u00(lat, p, u00)
!----------------------------------------------------------------------- 
! 
! Purpose: 
! Compute minimum relative humidity
! 
! Author: Minghua Zhang
!----------------------------------------------------------------------- 
! 
   real(r8), intent(in) :: lat(pcols)
   real(r8), intent(in) :: p(pcols,pver)

   real(r8), intent(out):: u00(pcols,pver)
    
    real(r8) u00tab(k0max,j0max)
    real(r8) latd(pcols)

    integer i,k
    integer k0,j0

    !
  latd = lat*180./3.1416
  do i=1,pcols
     j0 = int((90._r8 - latd(i))/2.) + 1
     j0 = min(j0,j0max)
    do k=1,pver
        k0 = int( p(i,k)/2500.)+1
        k0 = min(k0,k0max)
        u00(i,k) = zmh_u00tab(k0,j0)
     enddo
  enddo
  return
  end subroutine zmh_u00

