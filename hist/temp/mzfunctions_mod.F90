real(r8) function zmh_ramp(x,x0,xint)

! =====================================================
!  =0 when x<x0
!  =1 when x>x0+xint
!  = linear in between
!
! -------------------------------------
! input
  implicit none
  real(r8), intent(in) :: x,x0,xint
  real(r8)  x2

   if(x < x0) then
     x2 = 0.
   else if(x > x0+xint)then
     x2=1.
   else
     x2 = (x-x0)/max(xint,1.e-20)
   endif
     x2 = min(x2,1.)

  zmh_ramp = x2

 return

 end function zmh_ramp

 
